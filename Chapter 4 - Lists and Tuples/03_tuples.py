# A tuple is an immutable data type in python. we can also think it as an immutable list

a = (1,2,5,6) # it can be classified as tuple since we used curly braces () and not square braces [] as in list
print(type(a))

b =(1,) # If we would have not inserted comma (,) with 1, it would've been identified as int type
print(type(b))

